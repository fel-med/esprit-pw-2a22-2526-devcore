let currentLang = localStorage.getItem('language') || 'fr';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('language', lang);
    translatePage();
}

function translatePage() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (el.tagName === 'SELECT') {
            // Translate each option by its data-i18n-value attribute
            Array.from(el.options).forEach(option => {
                const optKey = option.getAttribute('data-i18n-value');
                if (optKey && translations[currentLang][optKey]) {
                    option.text = translations[currentLang][optKey];
                }
            });
        } else if (el.tagName === 'INPUT' && el.placeholder !== undefined && !el.id?.includes('search')) {
            if (translations[currentLang][key]) {
                el.placeholder = translations[currentLang][key];
            }
        } else {
            if (translations[currentLang][key]) {
                el.textContent = translations[currentLang][key];
            }
        }
    });
    
    // Traduire les placeholders spécifiques
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[currentLang][key]) {
            el.placeholder = translations[currentLang][key];
        }
    });
    
    const langBtn = document.getElementById('languageToggle');
    if (langBtn) {
        langBtn.textContent = currentLang === 'fr' ? '🇫🇷 FR' : '🇬🇧 EN';
    }
}

function toggleLanguage() {
    const newLang = currentLang === 'fr' ? 'en' : 'fr';
    setLanguage(newLang);
}

function initLanguage() {
    setLanguage(currentLang);
}